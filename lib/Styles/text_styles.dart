import 'package:flutter/cupertino.dart';

TextStyle infoLabel(Color c){
  return TextStyle(color: c, fontSize: 16 , fontWeight: FontWeight.w700);
}
TextStyle infoLabelMedium(Color c){
  return TextStyle(color: c, fontSize: 14 , fontWeight: FontWeight.w700);
}
TextStyle infoValue(Color c){
  return TextStyle(color: c, fontSize: 14);
}

TextStyle infoValueMedium(Color c){
  return TextStyle(color: c, fontSize: 12);
}

TextStyle infoValueSmall(Color c){
  return TextStyle(color: c, fontSize: 10);
}